"""**Question 6: Inventory Management System**

Concepts Used: Dictionaries, functions, conditionals
"""

def update_inventory(inventory, item, quantity):
    if item in inventory:
        inventory[item] = max(0, inventory[item] + quantity)
    else:
        print(f"{item} is not in the inventory.")
    return inventory
inventory = {
    "apple": 10,
    "banana": 5,
    "orange": 8,
    "grape": 12,
    "mango": 6
}
for _ in range(3):
    item_name = input("Enter item name: ")
    item_quantity = int(input("Enter quantity to add/remove: "))
    inventory = update_inventory(inventory, item_name, item_quantity)
print("Updated Inventory:")
for item, quantity in inventory.items():
    print(f"{item}: {quantity}")